  private void btnEjecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEjecutarActionPerformed
    try {
      // Datos
      String cuenta = txtCuenta.getText();
      // Data
      CuentaController control = new CuentaController();
      List<Map<String,?>> lista = control.obtenerMovimientos(cuenta);
      if(lista.isEmpty()){
        Mensajes.showInfo(rootPane, "Cuenta no existe.");
        return;
      }
      JRMapCollectionDataSource data;
      data = new JRMapCollectionDataSource(lista);
      // Par√°metros
      Map<String,Object> parms = new HashMap<>();
      // Archivo del reporte
      String reportFile= "/pe/egcc/eurekaapp/reports/repoMovimientos.jasper";
      InputStream reportStream = Class.class.getResourceAsStream(reportFile);
      // Construir el JasperPrint
      JasperPrint reportPrint;
      reportPrint = JasperFillManager.fillReport(reportStream, parms, data);
      // Visualizar el reporte
      panelReporte.removeAll();
      ReportViewInternal viewer;
      viewer = new ReportViewInternal("REPORTE DE MOVIMIENTOS");
      viewer.setReporte(reportPrint);
      panelReporte.add(viewer);
      viewer.setMaximum(true);
      //viewer.pack();
      viewer.setVisible(true);
    } catch (Exception e) {
      e.printStackTrace();
    }
  }//GEN-LAST:event_btnEjecutarActionPerformed


